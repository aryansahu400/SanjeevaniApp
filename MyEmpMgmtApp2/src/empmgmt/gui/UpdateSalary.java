    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String empNo=txtEmpNo.getText().trim();
        String empSal=txtEmpSal.getText().trim();
        if(empNo==null || empSal==null){
            JOptionPane.showMessageDialog(null, "Please input Values","Error",JOptionPane.ERROR);
        }else{
            try{
                int eno=Integer.parseInt(empNo);
                int sal=Integer.parseInt(empSal);
                EmpPojo emp=new EmpPojo(eno,"",sal);
                if(EmpDao.updateEmployeeSal(emp)){
                    JOptionPane.showMessageDialog(null, "SALARY UPDATED!","Success",JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Unknown Error occured","Error",JOptionPane.ERROR);
        
                }
            }catch(SQLException e){
                e.printStackTrace();
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null,"Pelase enter a valid employee id","Input Invalid",JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

