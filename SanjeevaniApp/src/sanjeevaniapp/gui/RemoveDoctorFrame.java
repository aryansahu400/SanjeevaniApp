    private void getIds() {
        List<String> list=null;
        try {
            list=DoctorsDao.getAllDoctorIds();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Exception in Remove Doctors Frame: "+ex.getMessage());
        }
        for(String id: list){
            jcbDoctorsId.addItem(id);
        }
        
    }
