    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean success=false;
        try {
            success=DoctorsDao.removeDoctorById(jcbDoctorsId.getSelectedItem().toString());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB error in remove doctors frame: "+ex.getMessage());
            ex.printStackTrace();
        }
        if(success){
            JOptionPane.showMessageDialog(null, "Doctor succesfully removed");
            jcbDoctorsId.removeItem(jcbDoctorsId.getSelectedItem().toString());
        }else{
            JOptionPane.showMessageDialog(null, "Doctor can not be removed");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

