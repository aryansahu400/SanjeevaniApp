    private void displayIds() {
        List<String> list;
        try{
            list=EmpDao.getAllEmployeeId();
        }catch(SQLException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null," Error in Database in remove frame: "+e.getMessage());
            return;
        }
        for(String id:list){
            jcbEmployeeId.addItem(id);
        }
    }
