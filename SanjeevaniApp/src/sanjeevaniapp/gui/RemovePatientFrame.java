    private void loadIds() {
       try{
            List<String>list =PatientDao.getAllIds();
            for(String p:list){
                this.jcbPatientId.addItem(p);
            }
       }catch(java.sql.SQLException ex){
            JOptionPane.showMessageDialog(null,"Database error in ViewSiblgePatientFrame:  "+ex.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
           return;
       }
    }
