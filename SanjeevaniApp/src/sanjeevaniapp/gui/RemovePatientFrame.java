    private void btnBack1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBack1ActionPerformed
        if(this.jcbPatientId.getItemCount()==1){
            JOptionPane.showMessageDialog(null, "No patients in the database");
            return;
        }
        String id=this.jcbPatientId.getSelectedItem().toString();
        if(id.equals("Patient Id")){
            JOptionPane.showMessageDialog(null, "Please Select an id");
            return;
        }
        try {
            if(PatientDao.removePatient(id)){
                JOptionPane.showMessageDialog(null, "Succesfully deleted"); 
                this.jcbPatientId.removeItem(id);
                this.jcbPatientId.setSelectedItem("Patient Id");}
            else JOptionPane.showMessageDialog(null, "Cannot delete at the moment, PLEASE TRY AGAIN LATER");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Database error in ViewSiblgePatientFrame:  "+ex.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnBack1ActionPerformed

