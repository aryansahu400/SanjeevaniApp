    private void btnVerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifyActionPerformed
        if(!otp.equals(this.txtOtp.getText())){
            JOptionPane.showMessageDialog(null,"Wrong OTP entered, PLEASE TRY AGAIN","Wrong OTP",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try {
            if(PatientDao.insertRecord(patient)){
                JOptionPane.showMessageDialog(null,"Patient added succesfully","Patient added",JOptionPane.PLAIN_MESSAGE);
                
            }else{
                JOptionPane.showMessageDialog(null,"Patient can not be added at the moment, PLEASE TRY AGAIN","TRY again",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        } catch (SQLException ex) {
             JOptionPane.showMessageDialog(null,"Database error in verify OTP Frame: "+ex.getMessage(),"TRY again",JOptionPane.INFORMATION_MESSAGE);
             ex.printStackTrace();   
             return;
        }
        new AddPatientFrame().setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_btnVerifyActionPerformed

