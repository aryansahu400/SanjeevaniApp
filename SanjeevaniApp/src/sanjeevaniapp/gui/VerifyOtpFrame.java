    private void btnSendOtpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendOtpActionPerformed
         otp=String.valueOf(new Random().nextInt(8999) + 1000);
         patient.setOtp(Integer.parseInt(otp));
         String mobileNumber=patient.getMobileNo();
        try {
            if(new OtpSender().send(mobileNumber, otp)){
                JOptionPane.showMessageDialog(null,"OTP sent");
            }else{
                JOptionPane.showMessageDialog(null,"Could not send the OTP, Please TRY AGAIN","TRY AGAIN",JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception ex) {
             JOptionPane.showMessageDialog(null,"Error while sending the opt :"+ex.getMessage(),"TRY AGAIN",JOptionPane.ERROR_MESSAGE);
             ex.printStackTrace();
              return;
        }
        this.txtOtp.setEditable(true);
    }//GEN-LAST:event_btnSendOtpActionPerformed

