    private void loadDetails() {
        model=(DefaultTableModel)jtTable.getModel();
        List<DoctorsPojo> list=null;
        try {
            list=DoctorsDao.getAllDoctorDetails();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"DB error in view doctors frame: "+ex.getLocalizedMessage());
        }
        for(DoctorsPojo doctor:list){
            Object[] arr=new Object[7];
            arr[0]=doctor.getDoctorId();
            arr[1]=doctor.getDoctorName();
            arr[2]=doctor.getGender();
            arr[3]=doctor.getEmailId();
            arr[4]=doctor.getContact();
            arr[5]=doctor.getQualification();
            arr[6]=doctor.getSpecilist();
            model.addRow(arr);
        }
        
    }
